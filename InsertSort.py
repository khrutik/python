L=[32,14,45,8,16]
S=[]

for x in L:
#3.1 หาตำแหน่งที่จะแทรกข้อมูลใน S
# x เก็บข้อมูลใน L ที่พิจารณา
    k=-1 # ยังไม่เจอที่แทรก
    for i in range(len(S)):
        if k==-1 and x<S[i]:
            k=i

    if k==-1:
        k=len(S)

#3.2 แทรกข้อมูลใน S ที่ตำแหน่งนั้น
    S.insert(k,x)

print(S)


